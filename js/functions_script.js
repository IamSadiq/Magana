	function submit_chat()
	{
        if (user_is_blocked) {
            //disable username field
            document.chat_form.msg.readOnly = true;  
        }
        else
        {
            message = document.chat_form.msg.value;

    		
    		var xmlhttp;

    		//Ensure all fields are filled
    		if (message == "") {

    			$('#empty_msg_span').show().fadeOut(2000);
    			return;
    		}
    		
    		if (window.XMLHttpRequest) {
    			// code for IE7+, Firefox, Chrome, Opera, Safari
    			xmlhttp = new XMLHttpRequest();
    			//alert('window xmlhttp called');
    		}
    		else
    		{
    			// code for IE6, IE5
    			xmlhttp = new ActiveXObject("Microsoft.XMLHTTP");
    			//alert('ActiveXObject xmlhttp called');
    		}

    		//vars = "username="+user_name+"&message="+message;
    		
    		
    		xmlhttp.onreadystatechange = function()
    		{
    			if (xmlhttp.readyState === 4 && xmlhttp.status === 200)
    				document.getElementById("chat_logs").innerHTML = xmlhttp.responseText;

    			/*
    				xmlhttp.readyState = 0 --> request not initialized
    				xmlhttp.readyState = 1 --> server connection established
    				xmlhttp.readyState = 2 --> request received
    				xmlhttp.readyState = 3 --> processing request
    				xmlhttp.readyState = 4 --> request finished and response is ready
    			*/
    		}

    		message = addslashes($.trim(message));
    		xmlhttp.open("GET", "server_file.php?message="+message, true);
    		//xmlhttp.setRequestHeader("Content-type", "application/x-www-form-urlencoded");
    		xmlhttp.send();

            //Empty your textbox
            document.chat_form.msg.value = "";

        }
	}